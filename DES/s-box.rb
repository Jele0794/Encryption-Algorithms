      def decArrToBinArr(sb)
         convertedArray = Array.new()
         j = 0 # Filas
         while j < 4
            i = 0
            pruebaArray = Array.new()
            while i < 16
               # Convierte de nÃºmero decimal a binario con de 4 bits
                  pruebaArray.push("%04b" % sb[j][i])
                  i += 1
            end
            convertedArray.push(pruebaArray)
            j += 1
         end
         return convertedArray
      end

